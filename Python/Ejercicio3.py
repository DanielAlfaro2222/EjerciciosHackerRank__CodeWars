"""
-----------------------------------------------------------------------------------------------------------------
Problema

El stub de código proporcionado lee dos enteros, a y b, desde STDIN.

Añade la lógica para imprimir dos líneas. La primera línea debe contener el resultado de la división de enteros, a // b. La segunda línea debe contener el resultado de la división de flotantes, a / b.

No es necesario redondear ni formatear.
"""

"""
-----------------------------------------------------------------------------------------------------------------
Solucion
"""

a = int(input())
b = int(input())

print(f"{a // b} \n{a / b}")
